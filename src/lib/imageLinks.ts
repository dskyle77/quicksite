export type ImageName = keyof typeof imageLinks;

export function getAvailableImageNames(): string[] {
  return Object.keys(imageLinks).sort();
}

export function getImageLink(name: string): string {
  return imageLinks[name as ImageName] || "";
}

export function injectImageLinks(obj: any): any {
  if (typeof obj !== "object" || obj === null) {
    if (typeof obj === "string" && obj.startsWith("i__")) {
      return getImageLink(obj);
    }
    return obj;
  }
  if (Array.isArray(obj)) {
    return obj.map((item) => injectImageLinks(item));
  }
  const result: any = {};
  for (const [key, value] of Object.entries(obj)) {
    result[key] = injectImageLinks(value);
  }
  return result;
}
